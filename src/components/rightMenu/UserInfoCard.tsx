import prisma from '@/lib/client';
import { auth } from '@clerk/nextjs/server';
import { User } from '@prisma/client';
import Image from 'next/image';
import Link from 'next/link';
import UserInfoCardInteraction from './UserInfoCardInteraction';
import UpdateUser from './UpdateUser';

const UserInfoCard = async ({ user }: { user: User }) => {

    const createAtDate = new Date(user.createdAt);

    const formattedDate = createAtDate.toLocaleDateString('en-US', {
        year: 'numeric',
        month: 'long',
        day: 'numeric',
    });

    let isUserBlocked = false;
    let isFollowing = false;
    let isFollowingSent = false;

    const { userId: currentUserId } = auth();

    if (currentUserId) {
        const blockRes = await prisma.block.findFirst({
            where: {
                blockerId: currentUserId,
                blockedId: user.id,
            },
        });

        blockRes ? (isUserBlocked = true) : (isUserBlocked = false);
        const followRes = await prisma.follower.findFirst({
            where: {
                followerId: currentUserId,
                followingId: user.id,
            },
        });

        followRes ? (isFollowing = true) : (isFollowing = false);
        const followReqRes = await prisma.followRequest.findFirst({
            where: {
                senderId: currentUserId,
                receiverId: user.id,
            },
        });

        followReqRes ? (isFollowingSent = true) : (isFollowingSent = false);
    }

    return (
        <div className="p-4 bg-white rounded-lg shadow-md text-sm flex flex-col gap-4">
            {/* TOP */}
            <div className="flex justify-between items-center font-medium">
                <span className='text-slate-600'>User Information</span>
                {currentUserId === user.id ? (
          <UpdateUser />
        ) : (
          <Link href="/" className="text-blue-500 text-xs">
            See all
          </Link>
        )}
            </div>
            {/* Bottom */}
            <div className="flex flex-col gap-4 text-slate-500">
                <div className="flex items-center gap-2">
                    <span className='text-xl text-black'>{user.name && user.surname ? user.name + " " + user.surname : user.username}</span>
                    <span className='text-sm'>@{user.username}</span>
                </div>
                {user.description && <p>{user.description}</p>}
                {user.city && <div className="flex items-center gap-2">
                    <Image src="/map.png" alt="alt" width={16} height={16} />
                    <span>Living In <b>{user.city}</b></span>
                </div>}
                {user.school && <div className="flex items-center gap-2">
                    <Image src="/school.png" alt="alt" width={16} height={16} />
                    <span>Went To <b>{user.school}</b></span>
                </div>}
                {user.work && <div className="flex items-center gap-2">
                    <Image src="/work.png" alt="alt" width={16} height={16} />
                    <span>Exploring <b>{user.work}</b></span>
                </div>}
                <div className="flex items-center justify-between">
                    {user.website && <div className="flex gap-1 items-center">
                        <Image src="/link.png" alt="alt" width={16} height={16} />
                        <Link href={user.website} className='text-purple-500 font-medium'>{user.website}</Link>
                    </div>}
                    <div className="flex gap-1 items-center">
                        <Image src="/date.png" alt="alt" width={16} height={16} />
                        <span>Joined {formattedDate}</span>
                    </div>
                </div>
                {currentUserId && currentUserId !== user.id && (
          <UserInfoCardInteraction
            userId={user.id}
            isUserBlocked={isUserBlocked}
            isFollowing={isFollowing}
            isFollowingSent={isFollowingSent}
          />
        )}
            </div>
        </div>
    );
}

export default UserInfoCard;